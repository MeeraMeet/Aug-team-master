package loops;

public class For {

	public static void main(String[] args) {
	// I want to print numbers say from 1 to 100
	// so read 1 print 1, read 2 print 2, ... 100 which means you have to start your print from int i =1; condition is i < =100; incrementor
	
		for (int i=1; i<=10; i++) // initializing a variable that you want to perform operation with/or ; write a condition ; increment/decrement
		{
			System.out.println(i); //statement
		}
		System.out.println("********* DECREMENTOR**********");
		
		for (int i=10; i>=1; i--) // initializing a variable that you want to perform operation with/or ; write a condition ; increment/decrement
		{
			System.out.println(i); //statement
		}
		
	}

}
