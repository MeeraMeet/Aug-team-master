package loops;

public class SwitchCase {

	public static void main(String[] args) {
	int number =20;
	
	switch (number) {
	case 1: System.out.println("1");
	break;
	case 3: System.out.println("3");
	break;
	case 10: System.out.println("10");
	break;
	default:
		System.out.println("23");
	}
	

	}

}
